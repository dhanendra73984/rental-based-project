// Define the interface for the transmission
interface Transmission {
  id: number;
  type: string;
}
// Define the interface for the fuel type
interface FuelType {
  id: number;
  type: string;
}
// Define the interface for the features
interface Features {
  features: string[];
}

interface BodyType {
  id: number;
  type: string;
}

interface Brand {
  id: number;
  name: string;
}

interface Description {
  description: string;
}

interface VehicleTypes {
  body_type: BodyType;
  brand: Brand;
  description: Description;
  id: number;
  model_name: string;
  release_year: number;
}

// Define the interface for the variant
export interface Variant {
  id: number;
  transmission: Transmission;
  fuel_type: FuelType;
  media_variant: any[]; // Update the type as needed
  prices: any[]; // Update the type as needed
  vehicle: VehicleTypes; // Update the type as needed
  name: string;
  power: string;
  num_of_doors: number;
  seating_capacity: number;
  range: number;
  color: string;
  ex_showroom_price: string;
  price_per_month_base: string;
  features: Features;
  created_at: string;
  modified_at: string;
}
// Define the interface for the API response
export interface ApiResponse {
  count: number;
  next: string | null;
  previous: string | null;
  results: Variant[];
}